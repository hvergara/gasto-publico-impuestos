    <h4  class="tk-adelle">Idea:</h4>
    
    <p><em>Gasto Público</em> nace bajo el evento Hackatón de Datos Abiertos, organizado por la Unidad de Modernización del Gobierno de Chile.</p>

    <p>Nuestra intención es explicar la Ley de Presupuesto Nacional de 2013, deglosando el impuesto único derivado del sueldo de cada chileno, utilizando herramientas de fácil comprensión a través de un lenguaje sencillo y cercano.</p>

    <p>Con el objetivo de empoderar al ciudadano del conocimiento de tanto el funcionamiento de los descuentos imponibles, como del sistema de distribución de impuestos para el Presupuesto Nacional.</p>

    <h4 class="tk-adelle">Futuro:</h4>
    <p>Pretendemos ser el portal de Gasto Público, combinando los datos presupuestarios como de gasto (Mercado Publico/ChileCompra).</p>

